class ShopMore {
	var $id;
	var $shopId;
	var $moreName;
	var $moreEmiPrice;
	var $morePrice;
	var $moreBeginTime;
	var $moreEndTime;
	var $moreDescription;
	var $moreImgName;
	var $moreCreateTime;
	var $moreOrder;

	public function getId() {
		return $this->id;
	}
	public function setId($id) {
		$this->id = $id;
	}

	public function getShopId() {
		return $this->shopId;
	}
	public function setShopId($shopId) {
		$this->shopId = $shopId;
	}

	public function getMoreName() {
		return $this->moreName;
	}
	public function setMoreName($moreName) {
		$this->moreName = $moreName;
	}

	public function getMoreEmiPrice() {
		return $this->moreEmiPrice;
	}
	public function setMoreEmiPrice($moreEmiPrice) {
		$this->moreEmiPrice = $moreEmiPrice;
	}

	public function getMorePrice() {
		return $this->morePrice;
	}
	public function setMorePrice($morePrice) {
		$this->morePrice = $morePrice;
	}

	public function getMoreBeginTime() {
		return $this->moreBeginTime;
	}
	public function setMoreBeginTime($moreBeginTime) {
		$this->moreBeginTime = $moreBeginTime;
	}

	public function getMoreEndTime() {
		return $this->moreEndTime;
	}
	public function setMoreEndTime($moreEndTime) {
		$this->moreEndTime = $moreEndTime;
	}

	public function getMoreDescription() {
		return null == $this->moreDescription ? "" : $this->moreDescription;
	}
	public function setMoreDescription($moreDescription) {
		$this->moreDescription = $moreDescription;
	}

	public function getMoreImgName() {
		return $this->moreImgName;
	}
	public function setMoreImgName($moreImgName) {
		$this->moreImgName = $moreImgName;
	}

	public function getMoreCreateTime() {
		return $this->moreCreateTime;
	}
	public function setMoreCreateTime($moreCreateTime) {
		$this->moreCreateTime = $moreCreateTime;
	}

	public function getMoreOrder() {
		return $this->moreOrder;
	}
	public function setMoreOrder($moreOrder) {
		$this->moreOrder = $moreOrder;
	}
}
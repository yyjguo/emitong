class Admin {
	var $id;
	var $adminName;
	var $adminPwd;
	var $adminEmail;
	var $adminPhone;
	var $adminGroup;
	var $adminRealName;

	public function getId() {
		return $this->id;
	}
	public function setId($id) {
		$this->id = $id;
	}

	public function getAdminName() {
		return $this->adminName;
	}
	public function setAdminName($adminName) {
		$this->adminName = $adminName;
	}

	public function getAdminPwd() {
		return $this->adminPwd;
	}
	public function setAdminPwd($adminPwd) {
		$this->adminPwd = $adminPwd;
	}

	public function getAdminEmail() {
		return $this->adminEmail;
	}
	public function setAdminEmail($adminEmail) {
		$this->adminEmail = $adminEmail;
	}

	public function getAdminPhone() {
		return $this->adminPhone;
	}
	public function setAdminPhone($adminPhone) {
		$this->adminPhone = $adminPhone;
	}

	public function getAdminGroup() {
		return $this->adminGroup;
	}
	public function setAdminGroup($adminGroup) {
		$this->adminGroup = $adminGroup;
	}

	public function getAdminRealName() {
		return null == $this->adminRealName ? "" : $this->adminRealName;
	}
	public function setAdminRealName($adminRealName) {
		$this->adminRealName = $adminRealName;
	}
}
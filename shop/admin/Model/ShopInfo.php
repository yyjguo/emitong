class ShopInfo {
	var $id;
	var $shopId;
	var $shopName;
	var $shopNickName;
	var $shopAddress;
	var $shopType;
	var $shopDescription;
	var $shopCreateTime;

	public function getId() {
		return $this->id;
	}
	public function setId($id) {
		$this->id = $id;
	}

	public function getShopId() {
		return $this->shopId;
	}
	public function setShopId($shopId) {
		$this->shopId = $shopId;
	}

	public function getShopName() {
		return $this->shopName;
	}
	public function setShopName($shopName) {
		$this->shopName = $shopName;
	}

	public function getShopNickName() {
		return null == $this->shopNickName ? "" : $this->shopNickName;
	}
	public function setShopNickName($shopNickName) {
		$this->shopNickName = $shopNickName;
	}

	public function getShopAddress() {
		return $this->shopAddress;
	}
	public function setShopAddress($shopAddress) {
		$this->shopAddress = $shopAddress;
	}

	public function getShopType() {
		return $this->shopType;
	}
	public function setShopType($shopType) {
		$this->shopType = $shopType;
	}

	public function getShopDescription() {
		return $this->shopDescription;
	}
	public function setShopDescription($shopDescription) {
		$this->shopDescription = $shopDescription;
	}

	public function getShopCreateTime() {
		return $this->shopCreateTime;
	}
	public function setShopCreateTime($shopCreateTime) {
		$this->shopCreateTime = $shopCreateTime;
	}

}
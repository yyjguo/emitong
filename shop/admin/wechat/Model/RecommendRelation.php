class RecommendRelation {
	private $id;
	private $recomm_open_id;
	private $recomm_user_id;
	private $open_id;
	private $user_id;
	private $recomm_time;

	/**
	 * @param mixed $id
	 */
	public function setId($id)
	{
		$this->id = $id;
	}

	/**
	 * @return mixed
	 */
	public function getId()
	{
		return $this->id;
	}

	/**
	 * @param mixed $open_id
	 */
	public function setOpenId($open_id)
	{
		$this->open_id = $open_id;
	}

	/**
	 * @return mixed
	 */
	public function getOpenId()
	{
		return $this->open_id;
	}

	/**
	 * @param mixed $recomm_open_id
	 */
	public function setRecommOpenId($recomm_open_id)
	{
		$this->recomm_open_id = $recomm_open_id;
	}

	/**
	 * @return mixed
	 */
	public function getRecommOpenId()
	{
		return $this->recomm_open_id;
	}

	/**
	 * @param mixed $recomm_time
	 */
	public function setRecommTime($recomm_time)
	{
		$this->recomm_time = $recomm_time;
	}

	/**
	 * @return mixed
	 */
	public function getRecommTime()
	{
		return $this->recomm_time;
	}

	/**
	 * @param mixed $recomm_user_id
	 */
	public function setRecommUserId($recomm_user_id)
	{
		$this->recomm_user_id = $recomm_user_id;
	}

	/**
	 * @return mixed
	 */
	public function getRecommUserId()
	{
		return $this->recomm_user_id;
	}

	/**
	 * @param mixed $user_id
	 */
	public function setUserId($user_id)
	{
		$this->user_id = $user_id;
	}

	/**
	 * @return mixed
	 */
	public function getUserId()
	{
		return $this->user_id;
	}


} 